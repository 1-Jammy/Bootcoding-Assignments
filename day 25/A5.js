console.log("Odd numbers from 0 to 100\n");
for (let i = 0; i <= 100; i++) {
    if (i % 2 !== 0) {
      console.log(i);
    }
}

console.log("----------------------------------\n");

console.log("Even numbers from 0 to 100\n");
for (let i = 0; i <= 100; i++) {
    if (i % 2 == 0) {
      console.log(i);
    }
}